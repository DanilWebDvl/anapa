    <p>Расширение и развитие спортивно-учебно-оздоровительного центра «ВОЛЕЙГРАД», расположенного в посёлке Витязево МО «город-курорт Анапа», как всероссийского центра волейбола, подразумевало создание на его базе профессиональной команды по классическому волейболу. В 2022 году, при поддержке Всероссийской федерации волейбола (ВФВ), мэрии Анапы и их партнёров, наряду с действующей Академией волейбола ВФВ, было принято решение об основании женского волейбольного клуба «Динамо-Анапа».</p>
    <p>Волейбольная история развития физкультурно-спортивного общества «Динамо» на территории Кубани неразрывно связана с созданием в 1946 году женской команды мастеров, которая быстро заработала авторитет одной из лучших команд страны. На сегодняшний день волейбольный клуб «Динамо» Краснодар – титулованная команда с богатой историей, выступающая в Суперлиге и подготовившая за время своего существования волейболисток, представлявших волейбол Краснодарского края в национальных сборных СССР и России. За 78 лет команда никогда не теряла своего исторического имени. Благодаря схожести целей и задач, а также в целях поддержки сложившихся традиций было решено дать нашему клубу соответствующее название «Динамо-Анапа».</p>
    <p>Президент ВК «Динамо-Анапа» – директор АНО «СУОЦ «ВОЛЕЙГРАД», Юрьев Егор Юрьевич.</p>
    <p>Пост генерального директора заняла именитая российская волейболистка Соколова Любовь Владимировна: двукратный серебряный призёр летних Олимпийских игр, двукратная чемпионка мира, Заслуженный мастер спорта. Главным тренером молодой и перспективной команды стал Алексей Юрьевич Гатин.</p>
    <p>Для организации полного цикла карьерного роста игроков от детского спорта к юношескому, а затем – в команду мастеров, перед клубом была поставлена цель создать фарм-команду, которая станет переходным этапом в подготовке и адаптации юных волейболисток к большому спорту. Усилия тренеров и руководства клуба направлены на воспитание юных игроков, их полноценный переход в команды Суперлиги и пополнение рядов сборных команд России (юношеская, молодёжная, национальная).</p>
    <p>Спортивный центр «ВОЛЕЙГРАД» стал домашней ареной клуба. Весь тренировочный процесс построен в рамках инфраструктуры спортивного комплекса, обладающего уникальными условиями для тренировок, отдыха и восстановления.</p>
    <p>Решением ВФВ команда «Динамо-Анапа» была включена в высшую лигу «А» чемпионата России. В дебютном сезоне анапская команда вышла в финальную стадию турнира, где в итоге заняла 5 место, а в следующем сезоне 2023-2024 спустилась на 7 строчку турнирной таблицы.</p>
    <p>Сезон 2024-2025 для команды стартует под эгидой нового главного тренера – Труша Александра Евгеньевича. Обновился и «омолодился» игровой состав: средний возраст игрока составляет 18 лет.</p>
    <p>21 октября 2023 года на территории «ВОЛЕЙГРАДА» состоялось торжественное открытие универсального спортивного комплекса «Волейград Арена» – площадью 7760 кв.м и вместимостью трибун на 1500 человек. В предстоящем сезоне наша команда начнет проводить здесь свои домашние туры.</p>
